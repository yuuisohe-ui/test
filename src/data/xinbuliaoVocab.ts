// 获取指定句子的词汇
export function getVocabForSentence(sentenceIndex: number): WordAnalysis[] {
  return xinbuliaoVocabAnalysis[sentenceIndex] || [];
}

// 获取所有词汇（去重）
export function getAllVocab(): WordAnalysis[] {
  const allVocab: WordAnalysis[] = [];
  const seenWords = new Set<string>();

  Object.values(xinbuliaoVocabAnalysis).forEach(vocabList => {
    vocabList.forEach(word => {
      if (!seenWords.has(word.word)) {
        seenWords.add(word.word);
        allVocab.push(word);
      }
    });
  });

  return allVocab;
}



// 获取所有词汇（去重）
export function getAllVocab(): WordAnalysis[] {
  const allVocab: WordAnalysis[] = [];
  const seenWords = new Set<string>();

  Object.values(guangnianVocabAnalysis).forEach(vocabList => {
    vocabList.forEach(word => {
      if (!seenWords.has(word.word)) {
        seenWords.add(word.word);
        allVocab.push(word);
      }
    });
  });

  return allVocab;
}

